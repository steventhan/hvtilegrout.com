const GettingStartedGoogleMap = withGoogleMap(props => (
  <GoogleMap
    defaultOptions={{
      scrollwheel: false,
      styles: mapStyle
    }}
    defaultZoom={15}
    defaultCenter={{ lat: info.address.lat, lng: info.address.lng }}
  >
    {props.markers.map(marker => (
      <Marker
        {...marker}
      >
        {marker.showInfo && (
          <InfoWindow onCloseClick={() => {}}>
            <div style={{color: "#090909"}} dangerouslySetInnerHTML={{__html: marker.infoContent}}></div>
          </InfoWindow>
        )}
      </Marker>
    ))}
  </GoogleMap>
));

class Map extends Component {
  constructor(props) {
    super(props);
    this.state = {
      markers: [{
        position: {
          lat: info.address.lat,
          lng: info.address.lng,
        },
        key: "Location",
        showInfo: true,
        infoContent: `<h1>${info.name}</h1><h2>${info.address.value}<h2>`,
        defaultAnimation: 2,
      }],
    };
  }

  render() {
    return (
      <div style={{height: "600px", marginTop: "2em"}}>
        <GettingStartedGoogleMap
          containerElement={
            <div style={{ height: "600px" }} />
          }
          mapElement={
            <div style={{ height: "600px" }} />
          }
          markers={this.state.markers}
        />
      </div>
    );
  }
}

class Location extends Component {
  render() {
    return (
      <section style={{display: "block"}}>
        <SectionHeaderText text="Location" center />
        <Map />
      </section>
    );
  }
}

export default Location;
